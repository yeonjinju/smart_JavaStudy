package 단항이항연산자;

public class Ex05비교연산자 {

	public static void main(String[] args) {
		// 비교연산자
		// <, <=, >, >=
		// ==, !=
		
		// == : 같니?
		// != : 다르니?
		
		// 결과값이 참/거짓 (boolean 형태)
		
		int a = 3;
		int b = 10;
		
		System.out.println(a==b);
		System.out.println(a!=b);
		System.out.println(a<b);
		
		
	}

}
